package week02_milena;

public class SwapNumbers {

    public static void main(String[] args) {

        int num1 = 6;
        int num2 = 14;

        num1 = num1+num2;  // 20
        num2 = num1-num2;  // 6
        num1 = num1-num2;  // 14

        System.out.println("Value of num1: " + num1);

        System.out.println("Value of num2: " + num2);
    }


}
